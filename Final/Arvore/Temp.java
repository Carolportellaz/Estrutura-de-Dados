public class Temp {
    private double graus;
    private int ano;
    
    public Temp(double graus, int ano) {
        this.graus = graus;
        this.ano = ano;
    }
    public double getGraus() {
        return graus;
    }
    public void setGraus(double graus) {
        this.graus = graus;
    }
    public int getAno() {
        return ano;
    }
    public void setAno(int ano) {
        this.ano = ano;
    }
}
